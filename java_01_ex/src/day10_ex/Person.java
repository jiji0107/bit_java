package day10_ex;

public class Person {
	
	public Person(String name, int age) {
		super();
		this.name = name;
		Age = age;
	}
	private String name;
	private int Age;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return Age;
	}
	public void setAge(int Age) {
		this.Age = Age;
	} 
	
}
